function validatePoSearchForm( )
{
	var objFV = new FormValidator("frmSearch");

	if (objFV.value("Po") == "" && objFV.value("Style") == "")
	{	
		if (!objFV.validate("Po", "B", "Please enter the Order No (PO) or Style No."))
			return false;
	}

	return true;
}