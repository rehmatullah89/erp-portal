function validateForm( )
{
	var objFV = new FormValidator("frmData");

	var sCheckboxes = $$("input.collections");
	var bChecked    = false;

	sCheckboxes.each( function(objElement)
	{
			if (objElement.checked == true)
				bChecked = true;
	});

	if (bChecked == false)
	{
		alert("Please select at-least 1 Collection to Add.");

		return false;
	}

	return true;
}