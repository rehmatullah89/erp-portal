function checkAll( )
{
	var sCheckboxes = $$("input.departments");

	sCheckboxes.each( function(objElement) { objElement.checked = true; } );
}

function clearAll( )
{
	var sCheckboxes = $$("input.departments");
	
	sCheckboxes.each( function(objElement) { objElement.checked = false; } );
}