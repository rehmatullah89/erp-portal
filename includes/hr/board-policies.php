						<li>Casual leave cannot be availed concurrently with Annual leave.</li>
						<li>CL should be availed to maximum of two days at a stretch.</li>
						<li>Un-availed CL can not be converted into any other type of leave and will be lost at the end of calendar year.</li>
						</ol>
						<br />
