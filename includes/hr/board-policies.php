					<h2>CODE OF CONDUCT</h2>

					<div style="padding:10px;">
						<h4>1. Objective</h4>
						Matrix Sourcing strives to maintain a work & learning environment for its staff in which honesty, integrity and respect for fellow employees and the customers/clients of the office are constantly reflected in the employees’ behavior, attitude and actions. The purpose of this document is to provide a clear framework within which employees are expected to conduct themselves. All staff also bear a responsibility as employees to act as ambassadors for the company in terms of their general conduct both within and outside the organization.<br />
						<br />
						This code covers some of the most important issues relating to personal conduct, and gives a framework of standards and behavioral guidelines, but it is not intended to be exhaustive.<br />
						<br />
						<br />

						<h4>2. Trust and Credibility</h4>
						The success of our business is dependent on the trust and confidence we earn from our employees, customers and shareholders. We gain credibility by adhering to our commitments, displaying honesty and integrity and reaching company goals solely through honorable conduct.<br />
						<br />
						<br />

						<h4>3. Equal Opportunities</h4>
						Matrix Sourcing seeks to ensure that the work environment for its employees is supportive and one where individual respect is shown to all. All members of staff, regardless of their gender, race, ethnic background, culture, (dis)ability, sexual orientation, age, religion, socio-economic status or any other factor will be treated equally, and will be supported and encouraged to perform to their potential.<br />
						<br />
						<br />

						<h4>4. A Culture of Open and Honest Communication</h4>
						At Matrix Sourcing everyone should feel comfortable to speak his or her mind, particularly with respect to ethical concerns. Managers have a responsibility to create an open and supportive environment where employees feel comfortable raising such questions. We all benefit tremendously when employees exercise their power to prevent mistakes or wrongdoing by asking the right questions at the right times.<br />
						<br />
						Matrix Sourcing will investigate all reported instances of questionable or unethical behavior. In every instance where improper behavior is found to have occurred, the company will take appropriate action. We will not tolerate retaliation against employees who raise genuine ethical concerns in good faith.<br />
						<br />
						Employees are encouraged, in the first instance, to address such issues with their managers or the HR manager, as most problems can be resolved swiftly.<br />
						<br />
						<br />

						<h4>5. Grievance Policy</h4>
						Matrix Sourcing is committed to ensuring that everyone is able to work without fear of harassment, bullying or intimidation. Everyone in the office has a part to play by ensuring that their own behavior, whether intentional or unintentional, does not constitute harassment. Any employee who feels harassed or discriminated against should report the incident to his or her manager or to human resources. Any other grievances, such as those relating to resources allocation and requirements, and any other unfair treatment should also be referred to the supervisor or directly to human resources.<br />
						<br />
						<br />

						<h4>6. Relationships With Other Members of Staff and Clients/Customers</h4>
						Matrix Sourcing does not concern itself with the private lives of its staff unless they affect its effective operation or its reputation.<br />
						<br />
						Members of staff who are relatives or who have a close personal relationship should not normally have a supervisory, assessing or authorizing relationship with each other. Employees must inform their line manager if they have a close personal relationship with another employee or a client/customer/supplier of Matrix Sourcing which could be considered by colleagues, or others, as impacting on the way they conduct themselves at work.<br />
						<br />
						Staffs are encouraged to socialize and develop professional relationships in the workplace provided that these relationships do not interfere with the work performance of either individual or with the effective functioning of the workplace. All employees need to be aware of their professional responsibilities and will be responsible for assuring that the relationship does not raise concerns about favoritism, bias, ethics and conflict of interest. In cases of doubt, advice and counsel should be sought from the next level of administrator.<br />
						<br />
						All interactions with the opposite gender must take into account the norms of our culture, society and those of a corporate work environment. A personal, non work-related relationship with team members of the opposite gender is discouraged due to the complications it may present for the organization. Any such relationship must be reported by the concerned parties to the respective supervisor immediately who should take it up with HR or Senior Management.<br />
						<br />
						<br />

						<h4>7. Drugs and Alcohol</h4>
						It is a disciplinary offence to be on Matrix Sourcing premises and/or carrying out official duties when under the influence of alcohol or non-medically prescribed drugs.<br />
						<br />
						<br />

						<h4>8. Conduct Outside Work</h4>
						Matrix Sourcing does not seek to dictate how employees conduct themselves in their personal lives outside work. However, unlawful, anti-social or other conduct by employees which may jeopardize Matrix Sourcing reputation or position will be seen negatively and will required disciplinary action.<br />
						<br />
						<br />

						<h4>9. Dress Code</h4>
						Employees must ensure that their dress is appropriate from a cultural and professional point of view. Shorts, open slippers etc. are not allowed in the office. Slippers are allowed around the designated prayer area(s) only; employees must ensure that they wear proper shoes once they finish with their prayers.<br />
						<br />
						<br />

						<h4>10. Etiquette</h4>
						Common courtesy, respect for others and rules of etiquette must apply in the workplace to ensure that colleagues can get along together and work productively. Employees should be encouraged to think before they speak and to apologize if they have made a mistake or offended a colleague. Boundaries, both physical and emotional, can help maintain a professional atmosphere. Use of abusive/foul language is strictly prohibited in the office.<br />
						<br />
						<br />

						<h4>11. Secondary Employment / Personal Business</h4>
						Employees should not perform work, offer consulting or managerial services for an organization that competes or does business with Matrix Sourcing. Involvement in personal / family business must be discussed with and approved by the supervisor / management.<br />
						<br />
						<br />

						<h4>12. Use of Company Resources</h4>
						Company resources, including time, material, equipment and information, are provided for work alone.<br />
						<br />
						Managers are responsible for the resources assigned to their departments and are empowered to resolve issues concerning their proper use.<br />
						<br />
						Employees must not use company equipment such as computers, copiers and fax machines for personal business or in support of any religious, political or other non work-related activity, except for company-requested support to nonprofit organizations. Employees must not distribute non-work related materials during work hours.<br />
						<br />
						In order to protect the interests of Matrix Sourcing, its clients and its employees, Matrix Sourcing reserves the right to monitor or review all data and information contained on an employee’s company-issued computer or electronic device. This also applies to the company provided email accounts.<br />
						<br />
						<br />

						<h4>13. Internet Policy</h4>
						Employees must not use the company provided internet access for anything other than work. The use of company resources to create, access, store, print, solicit or send any materials that are harassing, threatening, abusive, sexually explicit or otherwise offensive or inappropriate is not acceptable and will lead to strict disciplinary action.<br />
						<br />
						<br />

						<h4>14. Social Media Policy</h4>
						Employees must ensure that they never divulge any confidential information related to Matrix Sourcing and any of its clients and suppliers online.The employees act as ambassadors for Matrix Sourcing, its clients’ brands and its suppliers online. Therefore, please refrain from making any comments about Matrix Sourcing , its clients’ brands and its suppliers online.<br />
						<br />
						<br />

						<h4>15. Financial Probity; Conflicts of Interest</h4>
						Your employment at Matrix Sourcing should not lead to any financial or non-financial benefits from any our of our clients and suppliers.<br />
						<br />
						Employees must avoid any relationship or activity that might impair, or even appear to impair, their ability to make objective and fair decisions when performing their jobs.<br />
						<br />
						Employees are not allowed to seek benefits or favors from the suppliers by virtue of their employment with Matrix Sourcing. This includes, but is not limited to, gifts, monetary rewards and samples.<br />
						<br />
						Employees are also not authorized to force our suppliers to buy from certain materials suppliers unless such suppliers are nominated by the client/brand. For special cases, exceptions should be discussed with the supervisor.<br />
						<br />
						Determining whether a conflict of interest exists is not always easy to do. Employees with a conflict of interest question should seek advice from HR or their supervisor.<br />
						<br />
						<br />

						<h4>16. Confidentiality and Non Disclosure</h4>
						All samples, designs, tech packs, pricing information for Matrix Sourcing, its clients and its suppliers must be kept confidential at all times. Release of any such information to unauthorized persons or organizations will lead to immediate termination from the job.<br />
						<br />
						<br />

						<h4>17. Breach of Code Of Conduct</h4>
						This code of conduct has been drawn up to provide a source of guidance to the employees, and can be amended at any time. A breach of this code of conduct can result in disciplinary action and can also lead to termination from employment.<br />
