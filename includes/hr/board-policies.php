						<li>Any medical leave consisting of more than two days must be verified by a Doctor (letter or prescription from a Doctor).</li>
						<li>Medical leaves are not cumulative vacations, and in all cases, the employee must notify the Company of his/her sickness within 24 hours directly or indirectly.</li>
						<li>ML prior to confirmation is discouraged and salary deduction may apply.</li>
						</ol>
						<h4>COMPENSATORY LEAVE</h4>
						In a case when an employee is asked by his/her Manager to work on a weekend, holiday or a public holiday, that employee is entitled to receive two additional days in his/her Annual Leave for every one such working holiday. To earn such Compensatory Leave prior approval of the Manager is required.<br />
						<br />
						<h4>MATERNITY LEAVE</h4>
						<ol>
						<li> Female employees become eligible for the maternity leave after the completion of twelve months of continuous service with the company.</li>
						<li> An employee is entitled to a maximum of ninety (90) consecutive days of maternity leave, up to three mature pregnancies.</li>
						<li> She shall be entitled to the pay and allowances at regular rate in respect of such absenteeism.</li>
						<li> Reinstatement (after the maternity leave) will be to the position most recently held if it still exists or a comparable one if it does not.</li>
						<li> Employee must complete the Leave Application Form (P-05) along with a certificate from the Doctor stating the Expected Delivery Date. The Leave form must be approved by the concerned Head of Department and submitted to Administration Department at least Four (4) weeks prior to intended commencement of Maternity Leave.</li>
						<li> If any excess leave is required on Medical grounds then excess leave will be treated as leave without pay.</li>
						<li> Employee availing Maternity Leave will not be eligible for annual leave in that calendar year.</li>
						<li> Employee availing Maternity Leave will not be eligible for any leave carryover in that calendar year.</li>
						<li> Maternity leave cannot be combined with any other leave.</li>
						<li> If an employee fails to report to work on the day scheduled to return, she may be terminated by the Company,considering her last say actually worked,as her last day for account clearance purposes.</li>
