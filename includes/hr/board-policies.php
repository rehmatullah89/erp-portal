			        <h2>ANNUAL LEAVE</h2>

		            <div style="padding:10px;">
						<ol>
						<li>Employees who have completed twelve months of continuous service with the company will be eligible for annual leave.The calendar year of service which starts from January 1st to 31st December shall be applicable for determining the entitlement. The entitlement of annual leave for all Confirmed Employees is 14 days.</li>
						<li>Annual Leave cannot be accumulated for the next year. At the end of the calendar year the non-availed leave will lapse automatically unless written approval is taken from the reporting Manager and Admin Department for carry over.</li>
						<li>AL cannot be accumulated for more than 30 days.</li>
						<li>An employee applying for an AL has to take approval from the reporting Manager and inform to administration department at least 10 days before the AL starts.</li>
						<li>At the C.E.O.’s discretion an employee may be refused his / her annual leave due to official needs.</li>
						<li>Public holidays occurring during an employee’s vacation will be calculated separately and will not be considered as part of the employee’s annual leave.</li>
						<li>Employees are not allowed to work for another employer during vacation, with or without pay.</li>
						<li>The Company may request the employee to return to work during the authorized vacation as dictated by work needs. The Company shall then provide him with the remaining days of vacation after circumstances that compelled him to return, no longer exist.</li>
						<li>The Manager should ensure while accepting the Leave Form that overlapping of leaves is avoided so that routine work does not suffer</li>
						<li>Employees may request for annual leave, by using prescribed forms (P-05) addressed to their Managers and sent for record to PAA Deptt.</li>
						</ol>
						<br />
						<h4>CASUAL LEAVES</h4>
						<ol>
						<li>A total of 10 casual leaves per year are authorized to the employees</li>
