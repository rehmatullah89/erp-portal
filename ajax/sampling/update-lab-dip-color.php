	$Id     = IO::intValue("Id");
	$Code   = IO::strValue("Code");
	$Color  = IO::strValue("Color");
	$sError = "";

	$sSQL = "SELECT id FROM tbl_lab_dip_colors WHERE id='$Id'";
	$objDb->query($sSQL);

