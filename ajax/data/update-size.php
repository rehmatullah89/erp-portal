	$objDbGlobal = new Database( );
	$objDb       = new Database( );

	$Id     = IO::intValue("Id");
	$Type   = IO::strValue("Type");
	$Size   = IO::strValue("Size");
	$sError = "";

	$sSQL = "SELECT id FROM tbl_sizes WHERE id='$Id'";
	$objDb->query($sSQL);

	if ($Id == 0 || $objDb->getCount( ) != 1)
	{
		print "ERROR|-|Invalid Size ID. Please select the proper Size to Edit.\n";
		exit( );
	}

	if (!@in_array($Type, array("Size", "Length", "Equipment")))
		$sError .= "- Invalid Type\n";

	if ($Size == "")
		$sError .= "- Invalid Size\n";

	if ($sError != "")
	{
		print "ERROR|-|Please provide the values of following field(s):\n\n$sError";
		exit( );
	}

	$sSQL  = "SELECT * FROM tbl_sizes WHERE size LIKE '$Size' AND type='$Type' AND id!='$Id'";

	if ($objDb->query($sSQL) == true)
	{
		if ($objDb->getCount( ) == 0)
		{
			$sSQL = "UPDATE tbl_sizes SET type='$Type', size='$Size' WHERE id='$Id'";

			if ($objDb->execute($sSQL) == true)
				print "OK|-|$Id|-|<div>The selected Size has been Updated successfully.</div>|-|$Size|-|$Type";

			else
				print "ERROR|-|A Database ERROR occured while processing your request.  \n\nPlease re-load your webpage and try again.";
		}

		else
			print "INFO|-|$Id|-|The specified Type & Size already exists in the System";
	}

