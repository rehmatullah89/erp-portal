	{
		print "ERROR|-|You havn\'t enough Rights to modify the selected Data.\n";
		exit( );
	}

	$objDbGlobal = new Database( );
	$objDb       = new Database( );


	$Id        = IO::intValue("Id");
	$Category  = IO::strValue("Category");
	$Stages    = IO::strValue("Stages");
	$sError    = "";

	$sSQL = "SELECT id FROM tbl_style_categories WHERE id='$Id'";
	$objDb->query($sSQL);

	if ($Id == 0 || $objDb->getCount( ) != 1)
	{
		print "ERROR|-|Invalid Category ID. Please select the proper Category to Edit.\n";
		exit( );
	}

	if ($Stages == "")
		$sError .= "- Invalid Stage(s) Selection\n";

	if ($Category == "")
		$sError .= "- Invalid Style Category\n";

	if ($sError != "")
	{
		print "ERROR|-|Please provide the values of following field(s):\n\n$sError";
		exit( );
	}


	$sSQL  = "SELECT * FROM tbl_style_categories WHERE category LIKE '$Category' AND id!='$Id'";

	if ($objDb->query($sSQL) == true)
	{
		if ($objDb->getCount( ) == 0)
		{
			$sSQL = "UPDATE tbl_style_categories SET category='$Category', stages='$Stages' WHERE id='$Id'";

			if ($objDb->execute($sSQL) == true)
			{
				$sStagesList = getList("tbl_production_stages", "id", "title");
				$iStages     = @explode(",", $Stages);
				$sStages     = "";


				foreach ($iStages as $iStage)
					$sStages .= ($sStagesList[$iStage]."<br />");

				print "OK|-|$Id|-|<div>The selected Style Category has been Updated successfully.</div>|-|$Category|-|$sStages";
			}

			else
				print "ERROR|-|A Database ERROR occured while processing your request.  \n\nPlease re-load your webpage and try again.";
		}

		else
			print "INFO|-|$Id|-|The specified Style Category already exists in the System";
	}

	else
		print "ERROR|-|A Database ERROR occured while processing your request.  \n\nPlease re-load your webpage and try again.";

