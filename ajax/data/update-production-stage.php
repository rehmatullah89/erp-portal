	$objDbGlobal = new Database( );
	$objDb       = new Database( );

	$Id     = IO::intValue("Id");
	$Title  = IO::strValue("Title");
	$Type   = IO::strValue("Type");
	$sError = "";

	$sSQL = "SELECT id FROM tbl_production_stages WHERE id='$Id'";
	$objDb->query($sSQL);

	if ($Id == 0 || $objDb->getCount( ) != 1)
	{
		print "ERROR|-|Invalid Stage ID. Please select the proper Stage to Edit.\n";
		exit( );
	}

	if (!@in_array($Type, array("P", "Q")))
		$sError .= "- Invalid Type\n";

	if ($Title == "")
		$sError .= "- Invalid Stage Title\n";

	if ($sError != "")
	{
		print "ERROR|-|Please provide the values of following field(s):\n\n$sError";
		exit( );
	}

	$sSQL  = "SELECT * FROM tbl_production_stages WHERE title LIKE '$Title' AND id!='$Id'";

	if ($objDb->query($sSQL) == true)
	{
		if ($objDb->getCount( ) == 0)
		{
			$sSQL = "UPDATE tbl_production_stages SET type='$Type', title='$Title' WHERE id='$Id'";

			if ($objDb->execute($sSQL) == true)
				print ("OK|-|$Id|-|<div>The selected Production Stage has been Updated successfully.</div>|-|$Title|-|".(($Type == "P") ? "%" : "pcs"));

			else
				print "ERROR|-|A Database ERROR occured while processing your request.  \n\nPlease re-load your webpage and try again.";
		}

		else
			print "INFO|-|$Id|-|The specified Stage Title already exists in the System";
	}

	else
		print "ERROR|-|A Database ERROR occured while processing your request.  \n\nPlease re-load your webpage and try again.";

