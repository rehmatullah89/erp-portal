	if ($objDb->query($sSQL) == true)
	{
		if ($objDb->getCount( ) == 0)
		{
			$sSQL = "UPDATE tbl_crc_departments SET parent_id='$Parent', department='$Department' WHERE id='$Id'";

			if ($objDb->execute($sSQL) == true)
