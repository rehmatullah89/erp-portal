	$objDbGlobal = new Database( );
	$objDb       = new Database( );

	$Id         = IO::intValue("Id");
	$Parent     = IO::intValue("Parent");
	$Department = IO::strValue("Department");
	$sError     = "";

	$sSQL = "SELECT id FROM tbl_crc_departments WHERE id='$Id'";
	$objDb->query($sSQL);

	if ($Id == 0 || $objDb->getCount( ) != 1)
	{
		print "ERROR|-|Invalid Department ID. Please select the proper Department to Edit.\n";
		exit( );
	}

	if ($Parent > 0)
	{
		$sSQL = "SELECT department FROM tbl_crc_departments WHERE id='$Parent'";
		$objDb->query($sSQL);

		if ($objDb->getCount( ) != 1)
			$sError .= "- Invalid Parent\n";

		else
